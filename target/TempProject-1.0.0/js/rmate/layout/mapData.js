    setInitData() {
    	$.ajax({
            url: 'http://localhost:8080/getPeopleData.do',
            method: 'get',
            contentType: "application/json; charset:UTF-8",
            dataType: 'json',
            success: (response) => {
                response.forEach(item => {
                	item["tot_children"] = item.m_children + item.w_children;
                	item["tot_adult"] = item.m_adult + item.w_adult;
                	item["tot_elder"] = item.m_elder + item.w_elder;
                	this.mapData.push(item);
                });
            },
            error: (error) => {
                console.log(error);
            }
        });
    }
    
